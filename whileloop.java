// this is the program to display all alphates  on 30-october-2023
public class whileloop { // main class
    public static void main(String[] args) { // main method
        int i = 1; // initialization with declaration
        char al = 'A'; // variable declaration with assignment
        while(i<=26){ // while condition
            System.out.println(al); // output
            i++; // incrementation
            al++; // incrementation
        }
    }
}

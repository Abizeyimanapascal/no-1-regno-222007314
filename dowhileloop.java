// this is the program to display all odd numbers less than 15  on 30-october-2023
public class dowhileloop { // main class
    public static void main(String[] args) { // main method
        int i = 1; // variable declation with assignment
        do{ // beggening of looping
            System.out.println(i); // output
            i = i+2; // increment
        } // end of loop
        while(i<=15); // condition
    }
}
